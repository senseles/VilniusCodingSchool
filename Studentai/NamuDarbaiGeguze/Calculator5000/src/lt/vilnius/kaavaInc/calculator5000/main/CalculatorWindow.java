    // Methods
    private void setOperand(String o)
    {
        pressedOnOperand = true;
        
        if (firstNumberActive)
        {
            firstNumber = Double.parseDouble(Output.getText());
            firstNumberActive = false;
            //digit = false;
            operand = o;
            Output.setText("");
        }
        else
        {
            calculate();
            operand = o;
        }
    }

    private void setNumber(int i)
    {
        pressedOnOperand = false;
        
        if (newNumber)
        {
            Output.setText("");
            newNumber = false;
        }

        Output.setText(Output.getText() + i);
    }

    private void calculate()
    {
        secondNumber = Double.parseDouble(Output.getText());
        
        if (firstNumber != 0 && secondNumber != 0 && operand != "")
        {

            switch (operand)
            {
                case "/":
                    firstNumber = firstNumber / secondNumber;
                    break;
                case "*":
                    firstNumber = firstNumber * secondNumber;
                    break;
                case "-":
                    firstNumber = firstNumber - secondNumber;
                    break;
                case "+":
                    firstNumber = firstNumber + secondNumber;
                    break;
            }

            Output.setText(Double.toString(firstNumber));
            operand = "";
            secondNumber = 0;
            newNumber = true; 
        }
    }

