    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (first.isEmpty() && second.isEmpty()) {
            t = Integer.parseInt(jTextField1.getText());
            t2 = Integer.parseInt(jTextField2.getText());
        }

        if (!first.isEmpty() && !second.isEmpty()) {
            String tmp = String.join("", first);
            t = Integer.parseInt(tmp);
            String tmp2 = String.join("", second);
            t2 = Integer.parseInt(tmp2);
        }
        if (count > 0) {
            t = tmp;
        }
        switch (veiksmas) {
            case "+":
                tmp = t + t2;
                jLabel3.setText("= " + (tmp));
                count += 1;
                break;
            case "-":
                tmp = t - t2;
                jLabel3.setText("= " + (tmp));
                count += 1;
                break;
            case "*":
                tmp = t * t2;
                jLabel3.setText("= " + (tmp));
                count += 1;
                break;
            case "/":
                tmp = t / t2;
                jLabel3.setText("= " + (tmp));
                count += 1;
                break;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

