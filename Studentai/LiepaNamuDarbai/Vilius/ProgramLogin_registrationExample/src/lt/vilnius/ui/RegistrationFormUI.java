    private void butSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSaveActionPerformed
     try{
        stmt = dbc.getConnection();
     } catch (SQLException | ClassNotFoundException ex) {
         Logger.getLogger(RegistrationFormUI.class.getName()).log(Level.SEVERE,null,ex);
     }
     try {
         if (jPasswordField1.getText().equals(jPasswordField2.getText())) {
             insertMethod(stmt, txtFirstNameField.getText(), txtLastNameField.getText(), 
                     txtUsernameField.getText(), txtAdressField.getText(),
                     txtPostcodeField.getText(), jPasswordField1.getText());
             this.setVisible(false);
             AccountPage acp = new AccountPage();
             try {
                 acp.setUserName(txtUsernameField.getText());
                } catch ( ClassNotFoundException ex) {
                 Logger.getLogger(RegistrationFormUI.class.getName()).log(Level.SEVERE,null, ex);
                }
             acp.setVisible(true);
            } else {
             txtWrongPasswordLabel.setText("nesutampa slaptažodis");
         }
     } catch (SQLException ex) {
         Logger.getLogger(RegistrationFormUI.class.getName()).log(Level.SEVERE,null, ex);
         }
    }//GEN-LAST:event_butSaveActionPerformed

