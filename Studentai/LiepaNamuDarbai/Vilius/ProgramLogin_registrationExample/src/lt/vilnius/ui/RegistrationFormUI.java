     private void insertMethod(Statement stmt, String FirstName, String LastName, String UserName, String Adress, String PostalCode, String password) throws SQLException {
        String sql = "INSERT INTO `Login`(`UserName`, `Password`,"
                + " `FirstName`, `LastName`, `Address`,"
                + " `PostalCode`) VALUES"
                + "('" + UserName + "',"
                + "'" + password + "',"
                + "'" + FirstName + "',"
                + "'" + LastName + "',"
                + "'" + Adress + "',"
                + "'" + PostalCode + "')";
              

        stmt.executeUpdate(sql);
        System.out.println("Duomenys Insertinti");
    }
    
    




