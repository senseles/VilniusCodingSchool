 private boolean loginMethod(Statement stmt, String txtFieldUsername, String txtFieldPassword) throws SQLException {
        System.out.println("login : " + txtFieldUsername);
        if (!txtFieldUsername.isEmpty() && !txtFieldPassword.isEmpty()) {
            String sql = "SELECT * FROM `login` WHERE `UserName`='" + txtFieldUsername + "' and `Password`='" + txtFieldPassword + "'";
            try {
                ResultSet rs = stmt.executeQuery(sql);
                rs.next();
                String id = rs.getString("id");
            } catch (SQLException ex) {
                return false;
            }
            return true;
        } else {
            return false;
        }
    }




