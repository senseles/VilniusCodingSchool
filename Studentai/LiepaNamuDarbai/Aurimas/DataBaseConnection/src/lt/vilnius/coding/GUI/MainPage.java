    public void conectionToDatabase() throws HeadlessException {
        try {
            Statement stmt = AllMethods.getStatment();
            SimbolEntity simbol = new SimbolEntity();
            //  simbol.UserName = jTextField1.getText();
            // simbol.Password = jTextField2.getText();
            ResultSet rs = stmt.executeQuery("SELECT * FROM mydatabase WHERE UserName = '" +jTextField1.getText() + "' AND Password = '" +jTextField2.getText() + "' ");
            if (rs.next()){
                simbol.UserName = rs.getString("UserName");
                simbol.Password = rs.getString("Password");
                simbol.FirstName = rs.getString("FirstName");
                simbol.LastName = rs.getString("LastName");
                simbol.BirthDay = valueOf(rs.getString("BirthDate"));
                simbol.Sex = rs.getString("Sex");
                this.setVisible(false);
                new ProfileReview().setVisible(true);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "UserName or password is incorrect");
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

