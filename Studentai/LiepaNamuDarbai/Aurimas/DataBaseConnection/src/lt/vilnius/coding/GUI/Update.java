    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 try {
            Statement stmt = AllMethods.getStatment();
            SimbolEntity simbol = new SimbolEntity();
        
          stmt.executeUpdate("UPDATE mydatabase SET UserName='"+jTextField1.getText()+"',Password='"+ jTextField2.getText()+"', FirstName='"+jTextField3.getText()+"',LastName ='"+jTextField4.getText()+"', BirthDate ='"+jTextField5.getText()+"', Sex = '"+SimbolEntity.Sex+"' WHERE UserName = '" +simbol.UserName + "' AND Password = '" +simbol.Password + "'");
          simbol.UserName = jTextField1.getText();
          simbol.Password = jTextField2.getText();
           ResultSet rs = stmt.executeQuery("SELECT * FROM mydatabase WHERE UserName = '" +simbol.UserName + "' AND Password = '" +simbol.Password + "' ");
            if (rs.next()){
                simbol.UserName = rs.getString("UserName");
                simbol.Password = rs.getString("Password");
                simbol.FirstName = rs.getString("FirstName");
                simbol.LastName = rs.getString("LastName");
                simbol.BirthDay = valueOf(rs.getString("BirthDate"));
                simbol.Sex = rs.getString("Sex");
            this.setVisible(false);
            new ProfileReview().setVisible(true);
           /// new ProfileReview().jTextField1.setText("labas pasauli");
            }
            else{
             JOptionPane.showMessageDialog(null, "UserName or password is incorrect");
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

